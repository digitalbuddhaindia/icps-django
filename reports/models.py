class ReportCategory(models.Model):
    YEARLY = 'YL'
    MONTHLY = 'ML'
    QUARTERLY = 'QL'
    
    CATEGORY_CHOICES = [
        (YEARLY, 'Yearly'),
        (MONTHLY, 'Monthly'),
        (QUARTERLY, 'Quarterly')
    ]
    name = models.CharField(max_length=200)
    category = models.CharField(max_length=2, choices=CATEGORY_CHOICES)
    
    def __str__(self):
        return self.name
    
    class Meta:
        db_table = "report_categories"
        

class ReportType(models.Model):
    name = models.CharField(max_length=200)
    report_category = models.ForeignKey(ReportCategory, on_delete=models.PROTECT)
    
